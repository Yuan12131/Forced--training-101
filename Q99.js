// ? Javascript에서 배열과 객체의 공통점

// 변수로 할당: 배열과 객체는 모두 변수에 할당할 수 있는 값이며, 변수에 저장된 값을 참조
// 메서드 및 속성 사용: 배열과 객체는 각각의 내장 메서드와 속성을 사용
// 동적 구조:배열과 객체는 동적으로 변경 가능한 구조, 요소 또는 속성을 추가하거나 제거

const numbers = [1, 2, 3];
console.log(numbers[0]); // 배열 인덱스를 사용하여 값에 접근

const person = { name: 'lee', age: 28 };
console.log(person.name); // 객체의 속성을 사용하여 값에 접근
